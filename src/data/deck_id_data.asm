/*
	\1  = *_DECK_ID                           - byte
	\2  = *DeckName                           - text ptr
	\3  = Duelist*Name                        - text ptr
	\4  = *_PIC ID                            - byte
	\5  = PRIZES_* number or 0                - byte
	\6  = special duel rule ID or NONE        - byte
	\7  = DECK_REQUIREMENT_* ID or NONE       - byte
	\8  = MUSIC_DUEL_THEME_* ID or MUSIC_STOP - byte
	\9  = DUELIST_INTRO_* ID                  - byte
	\10 = COIN_* ID                           - byte

12 bytes
*/
MACRO deck_duel
	db \1
	tx \2
	tx \3
	db \4, \5, \6, \7, \8, \9, \<10>
ENDM

