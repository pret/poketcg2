/*
NPC_DUELIST_STRUCT_*
	\1 = NPC_* ID                           - byte
	\2 = Dialog*Text name                   - text ptr
	\3 = (short) home location text or NONE - text ptr
	\4 = (short) title text or NONE         - text ptr
	\5-\9 = *_DECK_ID or DECK_NONE          - byte

12 bytes
*/
MACRO npc_duelist
	db \1
	tx \2
	tx \3
	tx \4
	db \5, \6, \7, \8, \9
ENDM

