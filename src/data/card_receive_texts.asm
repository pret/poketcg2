/*
CARD_RECEIVE_STRUCT_*
	\1 = card ID                    - word
	\2 = long card name on msg box  - text ptr
	\3 = short card name on msg box - text ptr
	\4 = Received*Text              - text ptr
	\5 = alt flag (Legendaries)     - bool
	\6 = alt Received*Text          - text ptr

11 bytes. Mostly \2 = \3
*/
MACRO receive_card_text
	dw \1
	tx \2
	tx \3
	tx \4
	db \5
	tx \6
ENDM

